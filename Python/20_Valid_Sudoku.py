from itertools import chain


def is_valid_sudoku(board: list[list[str]]) -> bool:
    for row in range(9):
        for col in range(9):
            item = board[row][col]
            if item == '.':
                continue
            if board[row].count(item) > 1:
                return False
            if [*zip(*board)][col].count(item) > 1:
                return False
            box_x = row // 3 * 3
            box_y = col // 3 * 3
            if [*chain(*[*zip(*board[box_x: box_x + 3])][box_y: box_y + 3])].count(item) > 1:
                return False
    return True


